// TestVersionsOverrideFilename_Suffix locks in "Filename
// rules": the file MUST end in _override.tf so terraform recognizes it for
// per-attribute merge. A non-override.tf would clash with the user's own
// terraform {} block at parse time ("duplicate terraform block").
func TestVersionsOverrideFilename_Suffix(t *testing.T) {
	if !strings.HasSuffix(VersionsOverrideFilename, "_override.tf") {
		t.Errorf("VersionsOverrideFilename must end in _override.tf, got %q", VersionsOverrideFilename)
	}
	if !strings.HasPrefix(VersionsOverrideFilename, "_tfv2_") {
		t.Errorf("VersionsOverrideFilename should be namespaced with _tfv2_ prefix, got %q", VersionsOverrideFilename)
	}
}

// TestWriteVersionsOverride_RejectsBadInputs ensures the function fails
// fast on missing or malformed inputs rather than emitting HCL that would
