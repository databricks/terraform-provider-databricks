// TestWriteTerraformRC_CreatesWorkDir documents that the function bootstraps
// the workDir if it does not yet exist. The runner generally creates run
// directories beforehand, but it is gentle on early callers (e.g. ad-hoc
// tests of this package) and harmless when the dir already exists.
func TestWriteTerraformRC_CreatesWorkDir(t *testing.T) {
	parent := t.TempDir()
	workDir := filepath.Join(parent, "missing", "subdir")
	mirror := t.TempDir()
	cache := t.TempDir()

	path, err := WriteTerraformRC(workDir, mirror, cache)
	if err != nil {
		t.Fatalf("WriteTerraformRC: %v", err)
	}
	if _, err := os.Stat(path); err != nil {
		t.Errorf("file not written: %v", err)
	}
}

// TestWriteTerraformRC_Idempotent ensures repeated calls overwrite cleanly
// without appending or duplicating directives.
func TestWriteTerraformRC_Idempotent(t *testing.T) {
	workDir := t.TempDir()
	mirror := t.TempDir()
	cache := t.TempDir()

	for i := range 3 {
		if _, err := WriteTerraformRC(workDir, mirror, cache); err != nil {
			t.Fatalf("iteration %d: %v", i, err)
		}
	}
	body, err := os.ReadFile(filepath.Join(workDir, TerraformRCFilename))
	if err != nil {
		t.Fatalf("ReadFile: %v", err)
	}
	got := string(body)
	for _, directive := range []string{"provider_installation", "filesystem_mirror", "plugin_cache_dir"} {
		if c := strings.Count(got, directive); c != 1 {
			t.Errorf("expected exactly 1 %q after %d writes, got %d", directive, 3, c)
		}
	}
}

// TestHCLString_EscapesSpecialChars confirms our defensive HCL escaping
// turns paths containing backslash or double-quote into a syntactically
// valid HCL string. We exercise the package-private helper via the public
// API by feeding such a path through a writeable temp dir.
func TestHCLString_EscapesSpecialChars(t *testing.T) {
	for _, tc := range []struct {
		in   string
		want string
	}{
		{`/plain/path`, `/plain/path`},
		{`/with"quote`, `/with\"quote`},
		{`/with\backslash`, `/with\\backslash`},
		{`both"and\there`, `both\"and\\there`},
	} {
		if got := hclString(tc.in); got != tc.want {
			t.Errorf("hclString(%q) = %q, want %q", tc.in, got, tc.want)
		}
	}
}

