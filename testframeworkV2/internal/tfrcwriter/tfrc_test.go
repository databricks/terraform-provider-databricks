// TestWriteTerraformRC_HappyPath pins the generated.terraformrc against
// every line that calls out as load-bearing. A regression on
// any of these strings would silently break terraform init:
//
// - filesystem_mirror.path (must be absolute, must be the configured
// mirrorRoot — not derived from workDir)
// - filesystem_mirror.include (NARROW; widening breaks user HCL
// declaring other providers "Narrow include")
// - direct.exclude (mirrors the include exactly; otherwise some
// providers route neither through mirror nor direct)
// - plugin_cache_dir (per-run; absolute; enables hardlink reuse — F2)
func TestWriteTerraformRC_HappyPath(t *testing.T) {
	workDir := t.TempDir()
	mirrorRoot := t.TempDir()
	pluginCacheDir := t.TempDir()

	path, err := WriteTerraformRC(workDir, mirrorRoot, pluginCacheDir)
	if err != nil {
		t.Fatalf("WriteTerraformRC: %v", err)
	}
	wantPath := filepath.Join(workDir, TerraformRCFilename)
	if path != wantPath {
		t.Errorf("returned path: got %q want %q", path, wantPath)
	}
	if got := filepath.Base(path); got != ".terraformrc" {
		t.Errorf("filename should be .terraformrc, got %q", got)
	}

	body, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile: %v", err)
	}
	got := string(body)

	for _, want := range []string{
		`# Generated by testframeworkV2.`,
		`provider_installation {`,
		`filesystem_mirror {`,
		`path    = "` + mirrorRoot + `"`,
		`include = ["registry.terraform.io/databricks/databricks"]`,
		`direct {`,
		`exclude = ["registry.terraform.io/databricks/databricks"]`,
		`plugin_cache_dir = "` + pluginCacheDir + `"`,
	} {
		if !strings.Contains(got, want) {
			t.Errorf("body should contain %q\n--- got ---\n%s", want, got)
		}
	}

	// Hard-stop on any of the rejected wider patterns. Catching this in a
	// unit test is far cheaper than reproducing issue #5672's "provider
	// not found in any of the search locations" against a live workspace.
	for _, forbidden := range []string{
		`include = ["registry.terraform.io/*/*"]`,
		`include = ["registry.terraform.io/databricks/*"]`,
		`exclude = ["registry.terraform.io/*/*"]`,
		`exclude = ["registry.terraform.io/databricks/*"]`,
	} {
		if strings.Contains(got, forbidden) {
			t.Errorf("body must not contain forbidden wider pattern %q\n--- got ---\n%s", forbidden, got)
		}
	}
}

// TestWriteTerraformRC_RejectsRelativePaths locks in ("Mirror
// path is absolute"). A relative path silently resolves against the
// subprocess cwd at terraform-run time, breaking mirror lookup. Same
// reasoning for plugin_cache_dir.
func TestWriteTerraformRC_RejectsRelativePaths(t *testing.T) {
	workDir := t.TempDir()
	for _, tc := range []struct {
		name           string
		mirrorRoot     string
		pluginCacheDir string
		wantSub        string
	}{
		{"relative mirror", "rel/mirror", filepath.Join(workDir, "plugins"), "mirrorRoot must be absolute"},
		{"relative cache", filepath.Join(workDir, "mirror"), "rel/cache", "pluginCacheDir must be absolute"},
	} {
		t.Run(tc.name, func(t *testing.T) {
			_, err := WriteTerraformRC(workDir, tc.mirrorRoot, tc.pluginCacheDir)
			if err == nil {
				t.Fatal("expected error, got nil")
			}
			if !strings.Contains(err.Error(), tc.wantSub) {
				t.Errorf("error %q does not contain %q", err.Error(), tc.wantSub)
			}
		})
	}
}

